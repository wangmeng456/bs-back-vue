<template>
  <div :class="className" :style="{height:height,width:width}" />
</template>

<script>
import echarts from 'echarts'
require('echarts/theme/macarons') // echarts theme
import resize from './mixins/resize'

const animationDuration = 6000

export default {
  mixins: [resize],
  props: {
    className: {
      type: String,
      default: 'chart'
    },
    width: {
      type: String,
      default: '100%'
    },
    height: {
      type: String,
      default: '132px'
    },
    chartData: {
      type: Object,
      required: true
    }
  },
  data() {
    return {
      chart: null
    }
  },
  watch: {
    chartData: {
      deep: true,
      handler(val) {
        this.setOptions(val)
      }
    }
  },
  mounted() {
    this.$nextTick(() => {
      this.initChart()
    })
  },
  beforeDestroy() {
    if (!this.chart) {
      return
    }
    this.chart.dispose()
    this.chart = null
  },
  methods: {
    initChart() {
      this.chart = echarts.init(this.$el, 'macarons')
      this.setOptions(this.chartData)
    },
    setOptions({ chartName, chartValue } = {}) {
      this.chart.setOption({
        textStyle: {
            fontSize: 6
        },
        tooltip: {
            formatter: '{a} {b} : {c}'
        },
        series: [
            {
                name: chartName,
                type: 'gauge',
                detail: {
                    formatter: '{value}',
                    fontSize: 14,
                    padding: [40,0,0,0]
                },
                data: [{value: chartValue, name: ''}]
            }
        ]
      })
    }
  }
}
</script>
